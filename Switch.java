
public class Switch {
    public static void main(String[] args) {
        
    
   // int option = 2;


        int day = 3;

        switch(day)
        {
            case 1:
                System.out.println("Monday");
                break;

            case 2:
                System.out.println("Tuesday");
                break;

            case 3:
                System.out.println("Wednesday");
                break;

            default:
                System.out.println("Invalid Day");
        }

    }

}
